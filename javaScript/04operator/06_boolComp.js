// bolleans and comparison operator 

// booleans 
// true, false 


// comparison operator 
// 1. <   2. >  3. <=   4. >=  5. =
let num1 = 4;
let num2 = 6;
num1 > num2 
console.log(num1>num2)      // false 

console.log(num1 == num2);  // false 

// == vs ===
// ==   chack only value that value are equal or not 
// ===  it check all properties
let a = 10;
let b = "10";
console.log(a == b);    // true 
console.log(a === b);    // false 


// != Vs !== 
let aa = 10;
let bb = "10";
console.log(aa != bb);    // false 
console.log(aa !== bb);    // true


